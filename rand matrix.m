function E = randM(a)
  E=zeros(a);
for i=1:a
 for j=1:a
  if rem(i,2)~=0 && rem(j,2)~=0
   r=rand;
   if r<=1/3
    E(i,j)=-1;
	elseif r<=2/3
	E(i,j)=0;
	else
	E(i,j)=1;
   end
  else
  E(i,j)=i+j;
  end
 end
end