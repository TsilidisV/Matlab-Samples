%{
Να γράψετε συνάρτηση που να δέχεται ως παραµέτρους µια συµβολοσειρά s και
έναν χαρακτήρα c και να επιστρέφει µια συµβολοσειρά στην οποία όλες οι εµφανίσεις του χαρακτήρα
’a’ να έχουν αντικατασταθεί από τον χαρακτήρα c.
%}

function str = a2c(s,c)
  n=length(s);
  str=s;
  for i=1:n
    if s(i)=='a'
      str(i)='c';
    endif
  endfor
endfunction
